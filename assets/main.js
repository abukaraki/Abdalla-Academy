function tokenSpan(className, value) {
  return `<span class="${className}">${value}</span>`;
}

function protectTokens(html, regex, className) {
  const tokens = [];
  const protectedHtml = html.replace(regex, (match) => {
    const key = `\uE000${tokens.length}\uE001`;
    tokens.push(tokenSpan(className, match));
    return key;
  });
  return { html: protectedHtml, tokens };
}

function restoreTokens(html, tokens) {
  return html.replace(/\uE000(\d+)\uE001/g, (_, index) => tokens[Number(index)] || "");
}

function createTokenStash() {
  const tokens = [];
  return {
    put(className, value) {
      const key = String.fromCharCode(0xE100 + tokens.length);
      tokens.push(tokenSpan(className, value));
      return key;
    },
    restore(html) {
      return html.replace(/[\uE100-\uF8FF]/g, (key) => tokens[key.charCodeAt(0) - 0xE100] || "");
    }
  };
}

function highlightHtml(code) {
  let html = escapeHtml(code);
  html = html.replace(/(&lt;!--[\s\S]*?--&gt;)/g, tokenSpan("tok-comment", "$1"));
  html = html.replace(/(&lt;\/?)([a-zA-Z][\w:-]*)([\s\S]*?)(\/?&gt;)/g, (_, open, tag, attrs, close) => {
    const highlightedAttrs = attrs
      .replace(/\s([a-zA-Z_:][-a-zA-Z0-9_:.]*)(=)/g, ` <span class="tok-attr">$1</span>$2`)
      .replace(/(&quot;.*?&quot;|&#039;.*?&#039;)/g, tokenSpan("tok-string", "$1"));
    return `${tokenSpan("tok-tag", open)}${tokenSpan("tok-name", tag)}${highlightedAttrs}${tokenSpan("tok-tag", close)}`;
  });
  return html;
}

function highlightCss(code) {
  let html = escapeHtml(code);
  const stash = createTokenStash();
  html = html.replace(/(\/\*[\s\S]*?\*\/)/g, (match) => stash.put("tok-comment", match));
  html = html.replace(/([.#]?[a-zA-Z_-][\w-]*)(?=\s*\{)/g, (match) => stash.put("tok-name", match));
  html = html.replace(/\b(color|background|display|grid|flex|padding|margin|border|box-shadow|font|width|height|min-height|place-items)\b(?=\s*:)/g, (match) => stash.put("tok-attr", match));
  html = html.replace(/(#[0-9a-fA-F]{3,8}|rgba?\([^)]+\)|\b\d+(?:\.\d+)?(?:px|rem|em|vh|vw|%)\b)/g, (match) => stash.put("tok-number", match));
  return stash.restore(html);
}

function highlightJs(code) {
  let html = escapeHtml(code);
  const stash = createTokenStash();
  html = html.replace(/(\/\/.*$)/gm, (match) => stash.put("tok-comment", match));
  html = html.replace(/(`(?:\\.|[^`])*`|&quot;(?:\\.|[^&])*?&quot;|&#039;(?:\\.|[^&])*?&#039;)/g, (match) => stash.put("tok-string", match));
  html = html.replace(/\b(const|let|var|function|return|if|else|for|while|class|new|async|await|try|catch|throw|document|console)\b/g, (match) => stash.put("tok-keyword", match));
  html = html.replace(/\b([a-zA-Z_$][\w$]*)(?=\s*\()/g, (match) => stash.put("tok-function", match));
  html = html.replace(/\b(id|name|className|textContent|innerHTML|addEventListener|getElementById|querySelector)\b/g, (match) => stash.put("tok-attr", match));
  html = html.replace(/\b(\d+(?:\.\d+)?)\b/g, (match) => stash.put("tok-number", match));
  return stash.restore(html);
}

function highlightPhp(code) {
  let html = escapeHtml(code);
  const stash = createTokenStash();
  html = html.replace(/(&lt;\?php|\?&gt;)/g, (match) => stash.put("tok-tag", match));
  html = html.replace(/(\/\/.*$|#.*$)/gm, (match) => stash.put("tok-comment", match));
  html = html.replace(/(&quot;(?:\\.|[^&])*?&quot;|&#039;(?:\\.|[^&])*?&#039;)/g, (match) => stash.put("tok-string", match));
  html = html.replace(/(\$[a-zA-Z_]\w*)/g, (match) => stash.put("tok-variable", match));
  html = html.replace(/\b(echo|foreach|as|if|else|function|return|class|public|private|protected|new|array|require|include)\b/g, (match) => stash.put("tok-keyword", match));
  html = html.replace(/\b([a-zA-Z_]\w*)(?=\s*\()/g, (match) => stash.put("tok-function", match));
  html = html.replace(/\b(\d+(?:\.\d+)?)\b/g, (match) => stash.put("tok-number", match));
  return stash.restore(html);
}

function highlightCpp(code) {
  let html = escapeHtml(code);
  const stash = createTokenStash();
  html = html.replace(/(\/\/.*$)/gm, (match) => stash.put("tok-comment", match));
  html = html.replace(/(&lt;[a-zA-Z0-9_.]+&gt;|&quot;(?:\\.|[^&])*?&quot;|&#039;(?:\\.|[^&])*?&#039;)/g, (match) => stash.put("tok-string", match));
  html = html.replace(/(#include|#define|#ifdef|#endif)/g, (match) => stash.put("tok-keyword", match));
  html = html.replace(/\b(using|namespace|int|return|for|while|if|else|class|public|private|void|const|auto|vector|string|include|std|cout|cin|endl)\b/g, (match) => stash.put("tok-keyword", match));
  html = html.replace(/\b([a-zA-Z_]\w*)(?=\s*\()/g, (match) => stash.put("tok-function", match));
  html = html.replace(/\b(\d+(?:\.\d+)?)\b/g, (match) => stash.put("tok-number", match));
  return stash.restore(html);
}

function buildStudioJsDocument(code) {
  const bridge = `<script>
    window.onerror = function(message, source, lineno) {
      parent.postMessage({ source: "academy-studio", kind: "error", message: String(message) + (lineno ? " line " + lineno : "") }, "*");
    };
    ["log", "warn", "error"].forEach(function(method) {
      var original = console[method];
      console[method] = function() {
        var text = Array.from(arguments).map(function(value) {
          try { return typeof value === "object" ? JSON.stringify(value) : String(value); }
          catch (error) { return String(value); }
        }).join(" ");
        parent.postMessage({ source: "academy-studio", kind: method, message: text }, "*");
        original.apply(console, arguments);
      };
    });
  <\/script>`;
  if (/<html[\s>]/i.test(code)) return code.replace(/<head[^>]*>/i, (match) => `${match}${bridge}`);
  return `<!doctype html><html><head><meta charset="utf-8">${bridge}<style>body{font-family:Arial,sans-serif;padding:28px;background:#f8fafc;color:#10242f}button{min-height:40px;border:0;border-radius:8px;padding:0 14px;background:#0f766e;color:white;font-weight:800}</style></head><body>${code}</body></html>`;
}

function analyzeStudioCode(language, code) {
  const issues = [];
  const add = (type, arTitle, enTitle, arMessage, enMessage) => {
    issues.push({
      type,
      title: currentLang === "ar" ? arTitle : enTitle,
      message: currentLang === "ar" ? arMessage : enMessage
    });
  };
  const trimmed = code.trim();

  if (language === "html") {
    if (!/<!doctype html>/i.test(code)) add("warn", "DOCTYPE مفقود", "Missing DOCTYPE", "يفضل وضع <!doctype html> في بداية الصفحة.", "Add <!doctype html> at the top of the page.");
    ["html", "head", "body"].forEach((tag) => {
      if (!new RegExp(`<${tag}[\\s>]`, "i").test(code)) add("warn", `وسم ${tag} غير موجود`, `Missing ${tag} tag`, `وجود <${tag}> يجعل الصفحة أكثر وضوحا للمتصفح.`, `<${tag}> makes the page clearer for the browser.`);
    });
    return {
      output: currentLang === "ar" ? "تم تشغيل HTML داخل المعاينة." : "HTML rendered in the preview.",
      issues
    };
  }

  if (language === "js") {
    [balanceIssue(code, "{", "}", currentLang === "ar" ? "الأقواس المتعرجة" : "curly braces"), balanceIssue(code, "(", ")", currentLang === "ar" ? "الأقواس" : "parentheses")].filter(Boolean).forEach((issue) => issues.push(issue));
    if (!/<script[\s>]/i.test(code) && /document\.|console\.|const |let |function /.test(code)) add("warn", "ملاحظة", "Note", "يمكنك كتابة JavaScript داخل <script> أو كتابة صفحة HTML كاملة.", "You can write JavaScript inside <script> or provide a full HTML page.");
    return {
      output: currentLang === "ar" ? "تم تشغيل JavaScript داخل المعاينة. رسائل console تظهر هنا." : "JavaScript ran in the preview. Console messages appear here.",
      issues
    };
  }

  if (language === "php") {
    if (!/<\?php/.test(code)) add("error", "وسم PHP مفقود", "Missing PHP tag", "ابدأ كود PHP بـ <?php.", "Start PHP code with <?php.");
    if (/\$_(GET|POST)/.test(code) && !/(htmlspecialchars|filter_input|filter_var)/.test(code)) add("warn", "تنظيف المدخلات", "Input sanitization", "عند استخدام GET أو POST أضف validation أو escaping.", "When using GET or POST, add validation or escaping.");
    if (trimmed && !/[;}]\s*(\?>)?$/.test(trimmed)) add("warn", "نهاية السطر", "Line ending", "أغلب أوامر PHP تنتهي بفاصلة منقوطة ;", "Most PHP statements end with a semicolon ;");
    return {
      output: currentLang === "ar"
        ? "جاهز لتشغيل PHP وقراءة النتيجة."
        : "Ready to run PHP and read the output.",
      issues
    };
  }

  if (language === "cpp") {
    if (!/#include\s*<iostream>/.test(code)) add("error", "iostream غير موجود", "Missing iostream", "أضف #include <iostream> لاستخدام cout وcin.", "Add #include <iostream> to use cout and cin.");
    if (!/int\s+main\s*\(/.test(code)) add("error", "main غير موجودة", "Missing main", "كل برنامج C++ يحتاج دالة int main().", "Every C++ program needs int main().");
    [balanceIssue(code, "{", "}", currentLang === "ar" ? "الأقواس المتعرجة" : "curly braces"), balanceIssue(code, "(", ")", currentLang === "ar" ? "الأقواس" : "parentheses")].filter(Boolean).forEach((issue) => issues.push(issue));
    return {
      output: currentLang === "ar" ? "جاهز لتشغيل C++." : "Ready to run C++.",
      issues
    };
  }

  return { output: "", issues };
}

