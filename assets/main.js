const content = window.SITE_CONTENT?.items || [];
const pageSize = 6;
const menuButton = document.querySelector(".nav-toggle");
const menu = document.querySelector("#site-menu");
const languageButton = document.querySelector(".language-toggle");
let currentLang = localStorage.getItem("academy-language") || "ar";

function text(value) {
  if (!value) return "";
  if (typeof value === "string") return value;
  return value[currentLang] || value.ar || value.en || "";
}

function renderBodySegment(segment) {
  if (!segment) return "";
  const value = String(segment).trim();
  if (value.startsWith("<")) return value;
  return `<p>${value}</p>`;
}

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("academy-language", lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  if (languageButton) languageButton.textContent = lang === "ar" ? "EN" : "AR";
  ensureNavigation();
  document.querySelectorAll(".brand-mark").forEach((node) => {
    const logo = document.createElement("img");
    logo.className = "brand-logo";
    logo.src = "assets/my-logo-fast.png";
    logo.alt = "Abdalla Abu Karaki logo";
    logo.onerror = () => logo.remove();
    node.replaceWith(logo);
  });

  document.querySelectorAll("[data-text]").forEach((node) => {
    const key = node.getAttribute("data-text");
    if (ui[lang][key]) node.textContent = ui[lang][key];
  });

  document.querySelectorAll(".brand small").forEach((node) => {
    node.textContent = ui[lang].brandSub;
  });

  document.querySelectorAll(".site-footer [data-text='footerText']").forEach((node) => {
    node.remove();
  });

  document.querySelectorAll(".page-hero .lead").forEach((node) => {
    if (!node.closest(".privacy-page") && !node.closest(".terms-page") && !node.closest(".contact-page")) {
      node.remove();
    }
  });

  renderAll();
}

function ensureNavigation() {
  if (!menu) return;
  const page = document.body.getAttribute("data-page") || "";
  const active = (...pages) => pages.includes(page) ? ` aria-current="page"` : "";
  const courseCards = content
    .filter((item) => item.type === "course")
    .slice(0, 3)
    .map((item) => {
      const image = item.thumbnail || item.icon || "assets/my-logo-fast.png";
      return `
        <a class="course-menu-card" href="content.html?id=${encodeURIComponent(item.id)}">
          <img src="${image}" alt="${escapeHtml(text(item.title))}" loading="lazy">
          <span>${escapeHtml(text(item.title))}</span>
        </a>
      `;
    })
    .join("");

  menu.innerHTML = `
    <div class="nav-dropdown">
      <a href="courses.html"${active("courses", "content")} data-nav-root="courses">${ui[currentLang].courses}</a>
      <div class="nav-dropdown-panel" aria-label="${ui[currentLang].courses}">
        ${courseCards}
        <a class="course-menu-more" href="courses.html">${ui[currentLang].more}</a>
      </div>
    </div>
    <a href="compiler.html"${active("compiler")}>${ui[currentLang].compiler}</a>
    <a href="errors.html"${active("errors")}>${ui[currentLang].errors}</a>
    <a href="ai.html"${active("ai")}>${ui[currentLang].ai}</a>
  `;
}

function cardAction(item) {
  if (item.type === "course") return ui[currentLang].openCourse;
  if (item.type === "lectures") return ui[currentLang].watch;
  if (item.type === "materials") return ui[currentLang].download;
  return ui[currentLang].readMore;
}

function courseLectures() {
  return content
    .filter((item) => item.type === "course" && item.playlist?.length)
    .flatMap((courseItem) => courseItem.playlist.map((lesson, index) => ({
      id: `${courseItem.id}-lesson-${index + 1}`,
      type: "lectures",
      date: courseItem.date,
      duration: currentLang === "ar" ? `محاضرة ${index + 1}` : `Lesson ${index + 1}`,
      level: courseItem.level,
      course: courseItem.title,
      title: lesson.title,
      summary: lesson.summary,
      thumbnail: courseItem.thumbnail,
      icon: courseItem.icon,
      href: `content.html?id=${encodeURIComponent(courseItem.id)}&lesson=${index + 1}`
    })));
}

function makeCard(item) {
  const article = document.createElement("article");
  article.className = "content-card";
  const image = item.thumbnail || item.images?.[0]?.src || item.icon || "";
  const href = item.href || `content.html?id=${encodeURIComponent(item.id)}`;
  const media = image ? `<a class="content-media" href="${href}"><img src="${image}" alt="${text(item.title)}" loading="lazy"></a>` : "";
  const icon = item.icon ? `<img class="content-icon" src="${item.icon}" alt="" loading="lazy">` : "";
  const course = item.course ? `<span>${text(item.course)}</span>` : "";
  const lessonCount = item.playlist?.length ? `<span>${item.playlist.length} ${ui[currentLang].lessons}</span>` : "";
  const date = item.date ? `<span>${item.date}</span>` : "";
  const duration = item.duration ? `<span>${item.duration}</span>` : "";
  const level = item.level ? `<span>${item.level}</span>` : "";
  article.innerHTML = `
    ${media}
    <div class="card-meta">
      ${icon}
      ${course}
      ${lessonCount}
      ${date}
      ${duration}
      ${level}
    </div>
    <h2><a href="${href}">${text(item.title)}</a></h2>
    <p>${text(item.summary)}</p>
    <a class="button small" href="${href}">${cardAction(item)}</a>
  `;
  return article;
}

function currentPage(root) {
  return Math.max(1, Number(root.dataset.page || new URLSearchParams(window.location.search).get("page") || 1));
}

function setPage(root, page) {
  root.dataset.page = String(page);
  const params = new URLSearchParams(window.location.search);
  if (page > 1) params.set("page", String(page));
  else params.delete("page");
  const query = params.toString();
  window.history.replaceState({}, "", `${window.location.pathname}${query ? `?${query}` : ""}`);
}

function removePager(root) {
  const next = root.nextElementSibling;
  if (next?.classList.contains("pager")) next.remove();
}

function renderPager(root, totalItems) {
  removePager(root);
  const totalPages = Math.ceil(totalItems / pageSize);
  if (totalPages <= 1) return;

  const page = Math.min(currentPage(root), totalPages);
  const pager = document.createElement("nav");
  pager.className = "pager";
  pager.setAttribute("aria-label", "Pagination");

  const makeButton = (label, targetPage, isActive = false, disabled = false) => {
    const button = document.createElement("button");
    button.type = "button";
    button.textContent = label;
    button.disabled = disabled;
    if (isActive) button.setAttribute("aria-current", "page");
    button.addEventListener("click", () => {
      setPage(root, targetPage);
      renderAll();
      root.scrollIntoView({ behavior: "smooth", block: "start" });
    });
    return button;
  };

  pager.appendChild(makeButton(ui[currentLang].previous, Math.max(1, page - 1), false, page === 1));
  for (let index = 1; index <= totalPages; index += 1) {
    pager.appendChild(makeButton(`${ui[currentLang].page} ${index}`, index, index === page));
  }
  pager.appendChild(makeButton(ui[currentLang].next, Math.min(totalPages, page + 1), false, page === totalPages));
  root.insertAdjacentElement("afterend", pager);
}

function renderPagedItems(root, items) {
  root.innerHTML = "";
  if (!items.length) {
    removePager(root);
    root.innerHTML = `<p class="empty-state">${ui[currentLang].searchEmpty}</p>`;
    return;
  }

  const totalPages = Math.max(1, Math.ceil(items.length / pageSize));
  const page = Math.min(currentPage(root), totalPages);
  root.dataset.page = String(page);
  const start = (page - 1) * pageSize;
  items.slice(start, start + pageSize).forEach((item) => root.appendChild(makeCard(item)));
  renderPager(root, items.length);
}

function renderCollection(type, root) {
  const search = document.querySelector("[data-search]");
  const query = (search?.value || "").trim().toLowerCase();
  if (type === "lectures") {
    const items = courseLectures()
      .filter((item) => `${text(item.title)} ${text(item.summary)} ${text(item.course)}`.toLowerCase().includes(query));
    renderPagedItems(root, items);
    return;
  }
  if (type === "errors") {
    const source = content.concat(courseLectures());
    const terms = /error|errors|bug|debug|syntax|exception|warning|خطأ|اخطاء|أخطاء|تصحيح|مشكلة/i;
    const items = source
      .filter((item) => terms.test(`${text(item.title)} ${text(item.summary)} ${(item.tags || []).join(" ")}`))
      .filter((item) => `${text(item.title)} ${text(item.summary)}`.toLowerCase().includes(query));
    renderPagedItems(root, items);
    return;
  }
  const byType = (item) => {
    if (type === "courses") return item.type === "course";
    if (type === "software") return item.category === "software" || item.tags?.includes("software");
    if (type === "videos") return item.mediaType === "video" || item.type === "lectures";
    return item.type === type;
  };
  const items = content
    .filter(byType)
    .filter((item) => `${text(item.title)} ${text(item.summary)}`.toLowerCase().includes(query));

  renderPagedItems(root, items);
}

