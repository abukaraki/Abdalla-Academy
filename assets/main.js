function setupAiChat() {
  const form = document.querySelector("[data-ai-chat-form]");
  if (!form || form.dataset.ready === "true") return;
  form.dataset.ready = "true";
  const input = form.querySelector("[data-ai-chat-input]");
  const log = document.querySelector("[data-ai-chat-log]");
  const append = (role, message) => {
    if (!log) return;
    const item = document.createElement("article");
    item.className = `ai-chat-message ${role}`;
    item.innerHTML = `<strong>${role === "user" ? "user" : "ai"}</strong><p>${escapeHtml(message)}</p>`;
    log.appendChild(item);
    log.scrollTop = log.scrollHeight;
  };
  append("ai", currentLang === "ar" ? "$ ready" : "$ ready");
  form.addEventListener("submit", async (event) => {
    event.preventDefault();
    const message = input?.value.trim();
    if (!message) return;
    input.value = "";
    append("user", message);
    append("ai", "$ thinking...");
    const loading = log?.lastElementChild;
    try {
      const response = await fetch("/api/ai", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ action: "chat", message, ui_language: currentLang })
      });
      const data = await response.json().catch(() => ({}));
      if (!response.ok) throw new Error(data.error || data.message || `HTTP ${response.status}`);
      if (loading) loading.querySelector("p").textContent = formatAiChatReply(data);
    } catch (error) {
      if (loading) loading.querySelector("p").textContent = currentLang === "ar" ? "المساعد غير متاح الآن. جرّب مرة ثانية بعد لحظات." : "The assistant is not available right now. Try again in a moment.";
    }
  });
}

function formatAiChatReply(data) {
  const parts = [];
  if (data?.explanation) parts.push(String(data.explanation));
  if (Array.isArray(data?.tips) && data.tips.length) {
    parts.push(data.tips.map((tip) => `- ${tip}`).join("\n"));
  }
  if (data?.code) parts.push(String(data.code));
  return parts.join("\n\n") || String(data?.title || "AI");
}

