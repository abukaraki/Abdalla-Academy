function injectBeforeClose(source, closeTag, insert) {
  if (!insert.trim()) return source;
  const pattern = new RegExp(closeTag, "i");
  if (pattern.test(source)) return source.replace(pattern, `${insert}\n$&`);
  return `${source}\n${insert}`;
}

